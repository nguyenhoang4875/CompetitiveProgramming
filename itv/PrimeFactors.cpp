bool isPrime(int n) {
    if (n < 2) return false;
    if (n == 2) return true;
    for (int i = 2; i * i <= n; i++) {
        if (n % i == 0) return false;
    }
    return true;
}

vi getPrimeFactors(int n) {
    vi ans;
    for (int i = 2; i * i <= n; i++) {
        while (n % i == 0) {
            ans.pb(i);
            n /= i;
        }
    }
    if (n != 1) ans.pb(n);
    return ans;
}

void solve() {
    cin >> n;
    vi factors = getPrimeFactors(n);
    for (auto e : factors) cout << e << " ";
    cout << el;
}

