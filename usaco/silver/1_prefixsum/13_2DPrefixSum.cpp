const long long oo = 2e18, mod = 1e9 + 7;
const int ms = 2e5 + 5;
int n, m, q;

void solve() {
    cin >> n >> m >> q;
    vvi a(n + 1, vi(m + 1));
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            cin >> a[i][j];
        }
    }

    vvi f(n + 1, vi(m + 1));  // prefixSum 2D array;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            f[i][j] = f[i - 1][j] + f[i][j - 1] - f[i - 1][j - 1] + a[i][j];
        }
    }

    auto get2dSum = [&](int x1, int y1, int x2, int y2) {
        return f[x2][y2] - f[x2][y1 - 1] - f[x1 - 1][y2] + f[x1 - 1][y1 - 1];
    };

    while (q--) {
        int x1, x2, y1, y2;
        cin >> x1 >> y1 >> x2 >> y2;
        cout << get2dSum(x1, y1, x2, y2) << el;
    }
}

