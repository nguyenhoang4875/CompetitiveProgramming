struct Dsu {
    int n;
    vector<int> parent, size;

    Dsu() {};
    Dsu(int _n) {
        n = _n;
        parent = vector<int>(n + 1);
        size = vector<int>(n + 1);
        for (int v = 1; v <= n; v++) makeSet(v);
    }

    void makeSet(int v) {
        parent[v] = v;
        size[v] = 1;  // init size = 1;
    }

    int findSet(int v) { return parent[v] = (v == parent[v] ? v : findSet(parent[v])); }
};

const long long oo = 2e18, mod = 1e9 + 7;
const int ms = 2e5 + 5;

void solve() {
    int n, m;
    cin >> n >> m;
    Dsu dsu(n + 1);
    while (m--) {
        char type;
        int x;
        cin >> type >> x;
        if (type == '?') {
            int ans = dsu.findSet(x);
            if (ans == n + 1) ans = -1;
            cout << ans << el;
        } else {
            dsu.parent[x] = dsu.findSet(x + 1);
        }
    }
}

